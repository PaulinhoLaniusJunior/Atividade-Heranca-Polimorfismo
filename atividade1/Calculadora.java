public class Calculadora {

    // Método somar: soma dois inteiros
    public int somar(int a, int b) {
        return a + b;
    }

    // Método somar: soma dois números decimais
    public double somar(double a, double b) {
        return a + b;
    }

    // Método somar: soma três inteiros
    public int somar(int a, int b, int c) {
        return a + b + c;
    }
}